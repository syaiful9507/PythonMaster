# convert list to tuple
name = [1, 2, 3]
print(name, type(name))
result = tuple(name)
print(result, type(result))

# conver dictionary to tuple
name = {'name':'Abizar', 'age':1.5}
print(name, type(name))
result = tuple(name)
print(result, type(result))

# convert set to tuple
name = {1, 3, 'three'}
print(name, type(name))
result = tuple(name)
print(result, type(result))